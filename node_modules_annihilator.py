import os
import shutil

deleted = 0
for root, dirs, files in os.walk('/home', topdown=True):
   for name in dirs:
       if name == 'node_modules':
           deleted += 1
           print(f'Deleted: {deleted}')
           try:
              shutil.rmtree(os.path.join(root, name))
           except OSError as e:
              print("Error: %s : %s" % (root, e.strerror))
